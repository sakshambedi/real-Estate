properties.forEach(property => {
    const propertyCard = document.createElement('div');
    propertyCard.className = 'property-card';

    const image = document.createElement('img');
    image.src = property.imagePath;
    image.alt = `Property ${property.id}`;
    propertyCard.appendChild(image);

    const information = document.createElement('div');
    information.className = 'information';

    const title = document.createElement('div');
    title.className = 'property-card-title';
    title.textContent = property.address;
    information.appendChild(title);

    const price = document.createElement('div');
    price.className = 'property-card-price';
    price.textContent = property.price;
    information.appendChild(price);

    const details = document.createElement('div');
    details.textContent = `${property.bed} Beds ${property.bath} Baths ${property.size} sqft`;
    information.appendChild(details);

    propertyCard.appendChild(information);

    // Append the property card to the container
    propertyContainer.appendChild(propertyCard);
});